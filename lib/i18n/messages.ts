function getNested(obj: Record<string, any>, path: string): string | undefined {
  const keys = path.split('.')
  let current: any = obj
  for (const key of keys) {
    current = current?.[key]
    if (current === undefined) return undefined
  }
  return typeof current === 'string' ? current : undefined
}

function interpolate(str: string, vars: Record<string, string | number>): string {
  let result = str
  for (const [key, value] of Object.entries(vars)) {
    result = result.replace(new RegExp(`\\{\\{${key}\\}\\}`, 'g'), String(value))
  }
  return result
}

const messagesMap = { de, fr, lb }

export function getMessage(locale: 'de' | 'fr' | 'lb', key: string, vars?: Record<string, string | number>): string {
  const msg = getNested(messagesMap[locale] as Record<string, any>, key)
  const fallback = getNested(de as Record<string, any>, key)
  const text = msg ?? fallback ?? key
  return vars ? interpolate(text, vars) : text
}
